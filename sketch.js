function setup() {
  createCanvas(700,480);
  
  // R,G,B values
  background (130, 210, 245);
  
  //take a big brush
  strokeWeight (20);
}

function draw() {
  
  let posX=mouseX
  let posY=mouseY
  point(posX,posY,)
  fill(posX,20,30,20,28)
  stroke(10,12,50,20,60)
  
  rect(mouseX,mouseY,55,80,20)
  fill(mouseX,mouseY,30,80,20)
  

  
  

}